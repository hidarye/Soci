const orderedArPairs = AR_TRANSLATIONS.sort((a, b) => b[0].length - a[0].length);

export function translateRuntimeText(text: string, locale: Locale): string {
  if (locale !== 'ar' || !text) return text;

  let output = text;
  for (const [source, target] of orderedArPairs) {
    if (!output.includes(source)) continue;
    output = output.split(source).join(target);
  }
  return output;
}
